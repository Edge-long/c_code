#include <stdio.h>

int main09()
{
	//二进制位置0或置1
	//将13的二进制序列的第5位修改为1后改回0

	int a = 13; //00000000000000000000000000001101
	int b = 1 << 4; //00000000000000000000000000010000
	//第5位修改为1
	a |= b; //00000000000000000000000000011101
	printf("%d\n", a);
	b = ~b; //11111111111111111111111111101111
	//第5位修改为0
	a &= b; //00000000000000000000000000001101
	printf("%d\n", a);

	return 0;
}